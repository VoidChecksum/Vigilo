const PURIFIER_PROMPT = promptLines.join("\n")

export function createPurifierAgent(model?: string): AgentConfig {
  return {
    name: "purifier",
    description: "False Positive Neutralization Agent - Filters out findings that human triagers would reject",
    mode: "subagent" as const,
    model: model ?? "anthropic/claude-sonnet-4-5",
    temperature: 0.1,
    maxTokens: 64000,
    prompt: PURIFIER_PROMPT,
    color: "#8B5CF6",
    thinking: { type: "enabled", budgetTokens: 16000 },
  }
}

export const createPurifierAgentFactory: AuditorFactory = (model: string) => {
  return createPurifierAgent(model)
}
