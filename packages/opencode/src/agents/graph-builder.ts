const GRAPH_BUILDER_PROMPT = promptLines.join("\n")

export function createGraphBuilderAgent(model?: string): AgentConfig {
  return {
    name: "graph-builder",
    description: "Graph Builder - Composes findings into ranked attack paths over the file-based .vigilo/ store and emits static Mermaid/DOT/Markdown graph artifacts",
    mode: "subagent" as const,
    model: model ?? "anthropic/claude-sonnet-4-5",
    temperature: 0.0,
    maxTokens: 64000,
    prompt: GRAPH_BUILDER_PROMPT,
    color: "#10B981",
    thinking: { type: "enabled", budgetTokens: 32000 },
  }
}

export const createGraphBuilderAgentFactory: AuditorFactory = (model: string) => {
  return createGraphBuilderAgent(model)
}
