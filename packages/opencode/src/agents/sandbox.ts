const SANDBOX_PROMPT = promptLines.join("\n")

export function createSandboxAgent(model?: string): AgentConfig {
  return {
    name: "sandbox",
    description: "Sandbox Manager - Runs audit tooling (forge, cast, ast-grep, slither, echidna) safely through the sandboxed runner: pinned cwd, hard timeout, output cap, scrubbed env, no shell",
    mode: "subagent" as const,
    model: model ?? "anthropic/claude-sonnet-4-5",
    temperature: 0.0,
    maxTokens: 64000,
    prompt: SANDBOX_PROMPT,
    color: "#7C3AED",
    thinking: { type: "enabled", budgetTokens: 16000 },
  }
}

export const createSandboxAgentFactory: AuditorFactory = (model: string) => {
  return createSandboxAgent(model)
}
