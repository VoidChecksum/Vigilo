const TRIAGE_PROMPT = promptLines.join("\n")

export function createTriageAgent(model?: string): AgentConfig {
  return {
    name: "triage",
    description: "Severity Assessment & Prioritization Agent - Assigns accurate severity and priority to findings",
    mode: "subagent" as const,
    model: model ?? "anthropic/claude-sonnet-4-5",
    temperature: 0.1,
    maxTokens: 64000,
    prompt: TRIAGE_PROMPT,
    color: "#F59E0B",
    thinking: { type: "enabled", budgetTokens: 16000 },
  }
}

export const createTriageAgentFactory: AuditorFactory = (model: string) => {
  return createTriageAgent(model)
}
