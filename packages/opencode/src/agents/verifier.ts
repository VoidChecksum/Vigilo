const VERIFIER_PROMPT = promptLines.join("\n")

export function createVerifierAgent(model?: string): AgentConfig {
  return {
    name: "verifier",
    description: "Multi-Stage Verification Agent - Cross-validates every finding with 5 verification stages",
    mode: "subagent" as const,
    model: model ?? "anthropic/claude-sonnet-4-5",
    temperature: 0.1,
    maxTokens: 64000,
    prompt: VERIFIER_PROMPT,
    color: "#10B981",
    thinking: { type: "enabled", budgetTokens: 16000 },
  }
}

export const createVerifierAgentFactory: AuditorFactory = (model: string) => {
  return createVerifierAgent(model)
}
