const VALIDATOR_PROMPT = promptLines.join("\n")

export function createValidatorAgent(model?: string): AgentConfig {
  return {
    name: "validator",
    description: "Finding Validation Agent - Confirms vulnerabilities using static analysis tools (Slither, Mythril)",
    mode: "subagent" as const,
    model: model ?? "anthropic/claude-sonnet-4-5",
    temperature: 0.1,
    maxTokens: 64000,
    prompt: VALIDATOR_PROMPT,
    color: "#06B6D4",
    thinking: { type: "enabled", budgetTokens: 16000 },
  }
}

export const createValidatorAgentFactory: AuditorFactory = (model: string) => {
  return createValidatorAgent(model)
}
